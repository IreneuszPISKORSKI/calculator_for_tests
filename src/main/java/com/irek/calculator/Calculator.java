package com.irek.calculator;

//Créer une classe Calculator
public class Calculator {

    //○ Faire une addition de deux entiers et retourner un résultat;
    public int addition(int a, int b){
        return a+b;
    }

    //○ Faire une soustraction de deux entiers et retourner un résultat;
    public int subtraction(int a, int b) {
        return a - b;
    }

    //○ Faire une multiplication de deux entiers et retourner un résultat;
    public int multiplication(int a, int b) {
        return a * b;
    }

    //○ Faire une division de deux entiers et retourner un résultat;

    public int division(int a, int b) {
        return a / b;
    }

}
